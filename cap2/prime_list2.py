# Lista de números primos entre 1 e 10
prime_list = [2, 3, 5, 7]

# Imprime a lista antes da adição
print(f"Números primos: {prime_list}")

# Adiciona o número 11 à lista usando o método append
prime_list.append(11)

# Imprime a lista após a adição
print(f"Números primos após adição: {prime_list}")